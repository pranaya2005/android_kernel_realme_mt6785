static void lcm_resume_power(void)
{
	display_bias_enable();
}

static void lcm_init(void)
{
	gpio_set_value_cansleep(GPIO_LCM_RST, 1);
	MDELAY(10);
	gpio_set_value_cansleep(GPIO_LCM_RST, 0);
	MDELAY(10);
	gpio_set_value_cansleep(GPIO_LCM_RST, 1);
	MDELAY(120);

	push_table(NULL, lcm_init_setting, sizeof(lcm_init_setting) / sizeof(struct LCM_setting_table), 1);
}

static void lcm_suspend(void)
{
	push_table(NULL, lcm_suspend_setting, sizeof(lcm_suspend_setting) / sizeof(struct LCM_setting_table), 1);
	MDELAY(10);
}

static void lcm_resume(void)
{
	lcm_init();
}

void lcm_set_reset_pin(bool bEnable)
{
	gpio_set_value_cansleep(GPIO_LCM_RST, bEnable);
	MDELAY(10);
}

struct LCM_DRIVER td4320_auo640_auo_fhdpp2310_lcm_drv = {
	.name = "td4320_auo640_auo_fhdpp2310_lcm_drv",
	.set_util_funcs = lcm_set_util_funcs,
	.get_params = lcm_get_params,
	.init = lcm_init,
	.suspend = lcm_suspend,
	.resume = lcm_resume,
	.init_power = lcm_init_power,
	.resume_power = lcm_resume_power,
	.suspend_power = lcm_suspend_power,
};
