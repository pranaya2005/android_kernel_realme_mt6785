static void lcm_init_power(void)
{
	display_bias_enable();
}

static void lcm_suspend_power(void)
{
	display_bias_disable();
}

static void lcm_resume_power(void)
{
	display_bias_enable();
}

static void lcm_init(void)
{
	SET_RESET_PIN(1);
	MDELAY(20);
	SET_RESET_PIN(0);
	MDELAY(20);
	SET_RESET_PIN(1);
	MDELAY(120);

	push_table(NULL, lcm_init_setting, sizeof(lcm_init_setting) / sizeof(struct LCM_setting_table), 1);
}

static void lcm_suspend(void)
{
	push_table(NULL, lcm_suspend_setting, sizeof(lcm_suspend_setting) / sizeof(struct LCM_setting_table), 1);
	MDELAY(10);
	SET_RESET_PIN(1);
	MDELAY(20);
	SET_RESET_PIN(0);
	MDELAY(20);
	SET_RESET_PIN(1);
	MDELAY(120);
}

static void lcm_resume(void)
{
	lcm_init();
}

static unsigned int lcm_compare_id(void)
{
  unsigned int manufacture_id = 0,version_id = 0,id = 0;
  unsigned char buffer[3];
  unsigned int array[16];
  unsigned int lcm_id = 0;

  SET_RESET_PIN(1);
  MDELAY(20);
  SET_RESET_PIN(0);
  MDELAY(20);
  SET_RESET_PIN(1);
  MDELAY(50);

  array[0] = 0x00033700;    /* read id return two byte,version and id */
  dsi_set_cmdq(array, 1, 1);

  read_reg_v2(0x04, buffer, 3);
  manufacture_id = buffer[0];
  version_id = buffer[1];
  id = buffer[2];
  lcm_id = (buffer[0] << 16) | (buffer[1] << 8) | buffer[2];
  printk("ft8201:read manufacture_id = 0x%x,version_id = 0x%x,id = 0x%x\n",manufacture_id,version_id,id);

  return ((lcm_id == 0x83102b) ? 1 : 0);
}

