static kal_uint32 return_sensor_id(void)
{
	kal_uint32 sensor_id = 0;
  //	kal_uint16 module_id = 0;

	sensor_id = ((read_cmos_sensor_byte(0x0000) << 8) | read_cmos_sensor_byte(0x0001));
	
//sensor_id = read_cmos_sensor_byte(0x0000);
  revision = read_cmos_sensor_byte(0x0002) % 0xA0;

	printk("[%s] sensor_id: 0x%x revision: 0x%x\n", __func__, sensor_id,revision);

	if ( 0x01 <= revision) {
		sensor_id += 1;
	}
    printk("2222222222222222222 [%s] sensor_id: 0x%x revision: 0x%x\n", __func__, sensor_id,revision);
	return sensor_id;
}
/*************************************************************************
 * FUNCTION
 *	get_imgsensor_id
 *
 * DESCRIPTION
 *	This function get the sensor ID
 *
 * PARAMETERS
 *	*sensorID : return the sensor ID
 *
 * RETURNS
 *	None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 get_imgsensor_id(UINT32 *sensor_id)
{
  kal_uint8 i = 0;
  kal_uint8 retry = 2;


  /* sensor have two i2c address 0x6c 0x6d & 0x21 0x20,
   *we should detect the module used i2c address
   */
  while (imgsensor_info.i2c_addr_table[i] != 0xff) {
    spin_lock(&imgsensor_drv_lock);
    imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
    spin_unlock(&imgsensor_drv_lock);
    do {
      *sensor_id = return_sensor_id();
      if (*sensor_id == imgsensor_info.sensor_id) {
#ifdef CONFIG_MTK_CAM_CAL
        //read_imx135_otp_mtk_fmt();
#endif
        printk("s5kgw1 i2c write id: 0x%x, ReadOut sensor id: 0x%x, imgsensor_info.sensor_id:0x%x.\n", imgsensor.i2c_write_id,*sensor_id,imgsensor_info.sensor_id);	
        //s5kgw1kwCheckLensVersion(imgsensor.i2c_write_id);
        return ERROR_NONE;
      }
      printk("s5kgw1 Read sensor id fail, i2c write id: 0x%x, ReadOut sensor id: 0x%x, imgsensor_info.sensor_id:0x%x.\n", imgsensor.i2c_write_id,*sensor_id,imgsensor_info.sensor_id);	
      retry--;
    } while(retry > 0);
    i++;
    retry = 1;
  }    
  if (*sensor_id != imgsensor_info.sensor_id) {

    *sensor_id = 0xFFFFFFFF;
    return ERROR_SENSOR_CONNECT_FAIL;
  }
  return ERROR_NONE;
}


/*************************************************************************
 * FUNCTION
 *	open
 *
 * DESCRIPTION
 *	This function initialize the registers of CMOS sensor
 *
 * PARAMETERS
 *	None
 *
 * RETURNS
 *	None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 open(void)
{
  kal_uint8 i = 0;
  kal_uint8 retry = 2;
  kal_uint16 sensor_id = 0;

  pr_debug("%s", __func__);

  /* sensor have two i2c address 0x6c 0x6d & 0x21 0x20,
   * we should detect the module used i2c address
   */
  while (imgsensor_info.i2c_addr_table[i] != 0xff) {
    spin_lock(&imgsensor_drv_lock);
    imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
    spin_unlock(&imgsensor_drv_lock);
    do {
      sensor_id = return_sensor_id();
      if (sensor_id == imgsensor_info.sensor_id) {
        printk("s5kgw1 open i2c write id: 0x%x, sensor id: 0x%x\n", imgsensor.i2c_write_id,sensor_id);
        break;
      }
      printk("s5kgw1 openRead sensor id fail, id: 0x%x, sensor id: 0x%x\n", imgsensor.i2c_write_id,sensor_id);
      retry--;
    } while(retry > 0);
    i++;
    if (sensor_id == imgsensor_info.sensor_id)
      break;
    retry = 2;
  }
  if (imgsensor_info.sensor_id != sensor_id)
    return ERROR_SENSOR_CONNECT_FAIL;

  //	AFRegulatorCtrl(0);
  //	AFRegulatorCtrl(1);
  // msleep(5);
#ifdef CONFIG_FAKE_DUAL_CAMERA
  //  if(openYuvSensor() < 0)
  //    printk("open yuv sensor fail!\n");
  // msleep(50);
#endif
  /* initail sequence write in  */
  sensor_init();

  spin_lock(&imgsensor_drv_lock);

  imgsensor.autoflicker_en = KAL_FALSE;
  imgsensor.sensor_mode = IMGSENSOR_MODE_INIT;
  imgsensor.pclk = imgsensor_info.pre.pclk;
  imgsensor.frame_length = imgsensor_info.pre.framelength;
  imgsensor.line_length = imgsensor_info.pre.linelength;
  imgsensor.min_frame_length = imgsensor_info.pre.framelength;
  imgsensor.dummy_pixel = 0;
  imgsensor.dummy_line = 0;
  imgsensor.ihdr_mode = 0;
  imgsensor.test_pattern = KAL_FALSE;
  imgsensor.current_fps = imgsensor_info.pre.max_framerate;
  spin_unlock(&imgsensor_drv_lock);

  return ERROR_NONE;
}				



