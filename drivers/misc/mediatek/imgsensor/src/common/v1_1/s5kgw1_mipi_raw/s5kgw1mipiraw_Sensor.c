static kal_uint16 read_cmos_sensor_byte(kal_uint16 addr)
{
  kal_uint16 get_byte=0;
  char pu_send_cmd[2] = {(char)(addr >> 8) , (char)(addr & 0xFF) };

  //kdSetI2CSpeed(imgsensor_info.i2c_speed); // Add this func to set i2c speed by each sensor
  iReadRegI2C(pu_send_cmd , 2, (u8*)&get_byte,1,imgsensor.i2c_write_id);
  return get_byte;
}
static void write_cmos_sensor(kal_uint16 addr, kal_uint16 para)
{
  char pusendcmd[4] = {
    (char)(addr >> 8), (char)(addr & 0xFF),
    (char)(para >> 8), (char)(para & 0xFF) };
  iWriteRegI2C(pusendcmd, 4, imgsensor.i2c_write_id);
}

static kal_uint16 read_cmos_sensor_8(kal_uint16 addr)
{
  kal_uint16 get_byte = 0;
  char pusendcmd[2] = { (char)(addr >> 8), (char)(addr & 0xFF) };

  iReadRegI2C(pusendcmd, 2, (u8 *) &get_byte, 1, imgsensor.i2c_write_id);
  return get_byte;
}

static void write_cmos_sensor_8(kal_uint16 addr, kal_uint8 para)
{
  char pusendcmd[3] = {
    (char)(addr >> 8), (char)(addr & 0xFF), (char)(para & 0xFF) };

  iWriteRegI2C(pusendcmd, 3, imgsensor.i2c_write_id);
}

static void set_dummy(void)
{
  pr_debug("dummyline = %d, dummypixels = %d\n",
      imgsensor.dummy_line, imgsensor.dummy_pixel);


  write_cmos_sensor(0x0340, imgsensor.frame_length);
  write_cmos_sensor(0x0342, imgsensor.line_length);
}				

static kal_uint16 table_write_cmos_sensor(kal_uint16 *para, kal_uint32 len)
{
  char puSendCmd[I2C_BUFFER_LEN];
  kal_uint32 tosend, IDX;
  kal_uint16 addr = 0, addr_last = 0, data;

  tosend = 0;
  IDX = 0;

  while (len > IDX) {
    addr = para[IDX];

    {
      puSendCmd[tosend++] = (char)(addr >> 8);
      puSendCmd[tosend++] = (char)(addr & 0xFF);
      data = para[IDX + 1];
      puSendCmd[tosend++] = (char)(data >> 8);
      puSendCmd[tosend++] = (char)(data & 0xFF);
      IDX += 2;
      addr_last = addr;

    }
#if MULTI_WRITE

    if ((I2C_BUFFER_LEN - tosend) < 4 || IDX == len || addr != addr_last) {
      iBurstWriteReg_multi(
          puSendCmd, tosend, imgsensor.i2c_write_id, 4,
          imgsensor_info.i2c_speed);
      tosend = 0;
    }
#else
    iWriteRegI2CTiming(puSendCmd, 4,
        imgsensor.i2c_write_id, imgsensor_info.i2c_speed);

    tosend = 0;

#endif
  }
  return 0;
}

static void set_max_framerate(UINT16 framerate, kal_bool min_framelength_en)
{

  kal_uint32 frame_length = imgsensor.frame_length;

  pr_debug("framerate = %d, min framelength should enable %d\n",
      framerate, min_framelength_en);

  frame_length = imgsensor.pclk / framerate * 10 / imgsensor.line_length;
  spin_lock(&imgsensor_drv_lock);
  if (frame_length >= imgsensor.min_frame_length)
    imgsensor.frame_length = frame_length;
  else
    imgsensor.frame_length = imgsensor.min_frame_length;

  imgsensor.dummy_line =
    imgsensor.frame_length - imgsensor.min_frame_length;

  if (imgsensor.frame_length > imgsensor_info.max_frame_length) {
    imgsensor.frame_length = imgsensor_info.max_frame_length;

    imgsensor.dummy_line =
      imgsensor.frame_length - imgsensor.min_frame_length;
  }
  if (min_framelength_en)
    imgsensor.min_frame_length = imgsensor.frame_length;
  spin_unlock(&imgsensor_drv_lock);
  set_dummy();
}				

static void check_streamoff(void)
{
  unsigned int i = 0;
  int timeout = (10000 / imgsensor.current_fps) + 1;

  mdelay(3);
  for (i = 0; i < timeout; i++) {
    if (read_cmos_sensor_8(0x0005) != 0xFF)
      mdelay(1);
    else
      break;
  }
  pr_debug("%s exit!\n", __func__);
}

static kal_uint32 streaming_control(kal_bool enable)
{
  pr_debug("streaming_enable(0=Sw Standby,1=streaming): %d\n", enable);

  if (enable) {
    write_cmos_sensor(0x0100, 0x0100);
  } else {
    write_cmos_sensor(0x0100, 0x0000);
    check_streamoff();
  }
  return ERROR_NONE;
}

static void write_shutter(kal_uint16 shutter)
{

  kal_uint16 realtime_fps = 0;

  spin_lock(&imgsensor_drv_lock);
  if (shutter > imgsensor.min_frame_length - imgsensor_info.margin)
    imgsensor.frame_length = shutter + imgsensor_info.margin;
  else
    imgsensor.frame_length = imgsensor.min_frame_length;
  if (imgsensor.frame_length > imgsensor_info.max_frame_length)
    imgsensor.frame_length = imgsensor_info.max_frame_length;
  spin_unlock(&imgsensor_drv_lock);
  shutter = (shutter < imgsensor_info.min_shutter) ? imgsensor_info.min_shutter : shutter;
  shutter = (shutter > (imgsensor_info.max_frame_length - imgsensor_info.margin)) ? (imgsensor_info.max_frame_length - imgsensor_info.margin) : shutter;

  if (imgsensor.autoflicker_en) {
    realtime_fps = imgsensor.pclk
      / imgsensor.line_length * 10 / imgsensor.frame_length;

    if (realtime_fps >= 297 && realtime_fps <= 305)
      set_max_framerate(296, 0);
    else if (realtime_fps >= 147 && realtime_fps <= 150)
      set_max_framerate(146, 0);
    else {

      write_cmos_sensor(0x0340, imgsensor.frame_length & 0xFFFF);
    }
  } else {

    write_cmos_sensor(0x0340, imgsensor.frame_length & 0xFFFF);
  }

  write_cmos_sensor(0x0202, shutter & 0xFFFF);
  pr_debug("Exit! shutter =%d, framelength =%d\n", shutter, imgsensor.frame_length);
}				


/*************************************************************************
 * FUNCTION
 *	set_shutter
 *
 * DESCRIPTION
 *	This function set e-shutter of sensor to change exposure time.
 *
 * PARAMETERS
 *	iShutter : exposured lines
 *
 * RETURNS
 *	None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static void set_shutter(kal_uint16 shutter)
{
  unsigned long flags;

  spin_lock_irqsave(&imgsensor_drv_lock, flags);
  imgsensor.shutter = shutter;
  spin_unlock_irqrestore(&imgsensor_drv_lock, flags);

  write_shutter(shutter);
}				

static kal_uint16 gain2reg(const kal_uint16 gain)
{
  kal_uint16 reg_gain = 0x0;

  reg_gain = gain / 2;
  return (kal_uint16) reg_gain;
}

/*************************************************************************
 * FUNCTION
 *	set_gain
 *
 * DESCRIPTION
 *	This function is to set global gain to sensor.
 *
 * PARAMETERS
 *	iGain : sensor global gain(base: 0x40)
 *
 * RETURNS
 *	the actually gain set to sensor.
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint16 set_gain(kal_uint16 gain)
{
  kal_uint16 reg_gain;

  if (gain < BASEGAIN || gain > 32 * BASEGAIN) {
    pr_debug("Error gain setting");
    if (gain < BASEGAIN)
      gain = BASEGAIN;
    else if (gain > 32 * BASEGAIN)
      gain = 32 * BASEGAIN;
  }

  reg_gain = gain2reg(gain);
  spin_lock(&imgsensor_drv_lock);
  imgsensor.gain = reg_gain;
  spin_unlock(&imgsensor_drv_lock);
  pr_debug("set_gain = %d , reg_gain = 0x%x\n ", gain, reg_gain);

  write_cmos_sensor(0x0204, (reg_gain&0xFFFF));
  return gain;
}	

static void set_mirror_flip(kal_uint8 image_mirror)
{

  kal_uint8 itemp;

  pr_debug("image_mirror = %d\n", image_mirror);
  itemp = read_cmos_sensor_8(0x0101);
  itemp &= ~0x03;

  switch (image_mirror) {

    case IMAGE_NORMAL:
      write_cmos_sensor_8(0x0101, itemp);
      break;

    case IMAGE_V_MIRROR:
      write_cmos_sensor_8(0x0101, itemp | 0x02);
      break;

    case IMAGE_H_MIRROR:
      write_cmos_sensor_8(0x0101, itemp | 0x01);
      break;

    case IMAGE_HV_MIRROR:
      write_cmos_sensor_8(0x0101, itemp | 0x03);
      break;
  }
}

